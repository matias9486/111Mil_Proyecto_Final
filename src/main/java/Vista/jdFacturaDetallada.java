    public void cargarDatos()
    {        
        try
        {                
            String col[] = {"Nro Item","Cantidad","Producto","Precio","Importe"};                                                 
            
            DefaultTableModel tableModel = new DefaultTableModel(col,0){
                @Override
                public boolean isCellEditable(int rowIndex,int columnIndex){return false;}
            };
            
            jtFacturas.setModel(tableModel);

            f.getDetalleFactura().forEach((df) -> 
            {                                   
                //"Nro Item","Cantidad","Producto","Precio"
                Object[] datos = {String.valueOf(df.getNroItem()), String.valueOf(df.getCantidad()),
                    df.getProducto().getNombre(),"$ "+ String.format( "%.2f",df.getProducto().getPrecio()), "$ "+ String.format( "%.2f",df.getCantidad()* df.getProducto().getPrecio()) };            
                tableModel.addRow(datos);                   
            });                          
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Se ha producido un error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
