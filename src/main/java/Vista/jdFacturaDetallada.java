    public jdFacturaDetallada(javax.swing.JDialog parent, boolean modal,int id) {
        super(parent, modal);
        initComponents();
        
        try
        {
            f=facturas.BuscarFacturaPorNro(id);
            if(f!=null)
            {
                cargarDatos();
                jtxtNroFactura.setText(String.valueOf(f.getNroFactura()));
                SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");	
                Date fecha= f.getFecha();
                
                jtxtFecha.setText(dateFormat.format(fecha));
                jtxtCliente.setText(f.getCliente().getApellido()+", "+ f.getCliente().getNombre());
                
                jtxtTotalSinIva.setText("$ "+String.format( "%.2f",f.getTotalSinIva()));
                
                jlblIva.setText("IVA("+ String.valueOf(f.getIva()) +"%)");
                double ivaCalculado=f.getTotalConIva()-f.getTotalSinIva();
                jtxtIva.setText("$"+String.format( "%.2f",ivaCalculado));
                
                jtxtTotalConIva.setText("$ "+String.format( "%.2f",f.getTotalConIva()));
            }
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Se ha producido un error", JOptionPane.ERROR_MESSAGE);
        }        
    }

